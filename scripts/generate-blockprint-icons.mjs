// Generate tree-shakeable TSX components for the Lia block-print icon set.
//
// Source: the cleaned currentColor SVGs from the lia-blockprint-icons repo.
// Each source SVG is a <mask> (black bg + white/black ink paths) plus a
// currentColor <rect> that references it. We extract the mask's inner markup
// and emit one tiny module per icon that binds it to the shared factory in
// create-blockprint-icon.tsx. One module per icon keeps named imports
// tree-shakeable; the factory keeps the per-file payload to just the mask data.
//
// Usage:  node scripts/generate-blockprint-icons.mjs
// Source dir override:  BLOCKPRINT_SRC=/path/to/svg-currentcolor node scripts/...
import { readdirSync, readFileSync, writeFileSync, rmSync, mkdirSync } from "node:fs"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO = join(__dirname, "..")

const SRC =
  process.env.BLOCKPRINT_SRC ||
  "/Users/danrobson/Code Projects/lia-blockprint-icons/output/final/svg-currentcolor"

const OUT_DIR = join(REPO, "src/components/blockprint-icons")
const GEN_DIR = join(OUT_DIR, "generated")

const pascal = (name) =>
  name
    .split(/[-_]/)
    .filter(Boolean)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join("")

const componentName = (name) => `Blockprint${pascal(name)}Icon`

const files = readdirSync(SRC)
  .filter((f) => f.endsWith(".svg"))
  .sort()

if (files.length === 0) {
  console.error(`No SVGs found in ${SRC}`)
  process.exit(1)
}

// fresh generated/ dir
rmSync(GEN_DIR, { recursive: true, force: true })
mkdirSync(GEN_DIR, { recursive: true })

const names = []
const indexLines = []
let skipped = 0

for (const file of files) {
  const base = file.replace(/\.svg$/, "")
  const svg = readFileSync(join(SRC, file), "utf8")
  const match = svg.match(/<mask\b[^>]*>([\s\S]*?)<\/mask>/)
  if (!match) {
    skipped++
    console.warn(`skip (no mask): ${file}`)
    continue
  }
  // Collapse whitespace — SVG path data only needs single-space separators,
  // and this trims the committed file size noticeably.
  const maskInner = match[1].replace(/\s+/g, " ").trim()
  const comp = componentName(base)

  const body = `import { createBlockprintIcon } from "../create-blockprint-icon"

export const ${comp} = createBlockprintIcon(
  ${JSON.stringify(maskInner)},
  ${JSON.stringify(base)},
)
`
  writeFileSync(join(GEN_DIR, `${base}.tsx`), body)
  indexLines.push(`export { ${comp} } from "./${base}"`)
  names.push(base)
}

// generated barrel
writeFileSync(
  join(GEN_DIR, "index.ts"),
  `// AUTO-GENERATED by scripts/generate-blockprint-icons.mjs — do not edit.\n${indexLines.join("\n")}\n`,
)

// names manifest (cheap string list — no icon data — for pickers/validation)
const namesLiteral = names.map((n) => `  ${JSON.stringify(n)},`).join("\n")
writeFileSync(
  join(OUT_DIR, "blockprint-icon-names.ts"),
  `// AUTO-GENERATED by scripts/generate-blockprint-icons.mjs — do not edit.
export const blockprintIconNames = [
${namesLiteral}
] as const

export type BlockprintIconName = (typeof blockprintIconNames)[number]
`,
)

console.log(
  `Generated ${names.length} block-print icon components${skipped ? ` (${skipped} skipped)` : ""}.`,
)
